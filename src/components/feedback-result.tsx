'use client';

import { generateEssayFeedback } from '@/ai/flows/generate-essay-feedback';
import type { GenerateEssayFeedbackOutput } from '@/ai/flows/generate-essay-feedback';
import { useEffect, useState } from 'react';
import {
  Card,
  CardContent,
  CardDescription,
  CardHeader,
  CardTitle,
} from './ui/card';
import { Progress } from './ui/progress';
import { Alert, AlertDescription, AlertTitle } from './ui/alert';
import { Terminal } from 'lucide-react';
import { Skeleton } from './ui/skeleton';

function ScoreDisplay({
  label,
  score,
}: {
  label: string;
  score: number;
}) {
  return (
    <div className="space-y-2">
      <div className="flex justify-between items-center">
        <span className="font-medium">{label}</span>
        <span className="font-bold text-primary text-lg">{score} / 5</span>
      </div>
      <Progress value={score * 20} className="h-3" />
    </div>
  );
}

export function FeedbackResult({ essay }: { essay: string }) {
  const [feedback, setFeedback] =
    useState<GenerateEssayFeedbackOutput | null>(null);
  const [error, setError] = useState<string | null>(null);

  useEffect(() => {
    const getFeedback = async () => {
      setError(null);
      try {
        const result = await generateEssayFeedback({ essayText: essay });
        setFeedback(result);
      } catch (e) {
        console.error(e);
        setError(
          'An error occurred while generating feedback. The AI may be busy. Please try again later.'
        );
      }
    };
    if (essay) {
      getFeedback();
    }
  }, [essay]);

  if (error) {
    return (
      <Alert variant="destructive">
        <Terminal className="h-4 w-4" />
        <AlertTitle>Error</AlertTitle>
        <AlertDescription>{error}</AlertDescription>
      </Alert>
    );
  }

  if (!feedback) {
    return <FeedbackSkeleton />;
  }

  return (
    <Card>
      <CardHeader>
        <CardTitle>Essay Evaluation</CardTitle>
        <CardDescription>
          This feedback is generated by an AI assistant.
        </CardDescription>
      </CardHeader>
      <CardContent className="space-y-6">
        <div className="space-y-4 rounded-lg border p-4">
          <ScoreDisplay label="Clarity" score={feedback.clarity} />
          <ScoreDisplay label="Relevance" score={feedback.relevance} />
          <ScoreDisplay label="Originality" score={feedback.originality} />
        </div>
        <div>
          <h3 className="font-semibold mb-2 text-lg">Qualitative Feedback</h3>
          <p className="text-muted-foreground whitespace-pre-wrap">
            {feedback.feedback}
          </p>
        </div>
      </CardContent>
    </Card>
  );
}

export function FeedbackSkeleton() {
  return (
    <Card>
      <CardHeader>
        <Skeleton className="h-7 w-48" />
        <Skeleton className="h-4 w-64" />
      </CardHeader>
      <CardContent className="space-y-6">
        <div className="space-y-4 rounded-lg border p-4">
          {[...Array(3)].map((_, i) => (
            <div key={i} className="space-y-2">
              <div className="flex justify-between items-center">
                <Skeleton className="h-5 w-24" />
                <Skeleton className="h-6 w-16" />
              </div>
              <Skeleton className="h-3 w-full" />
            </div>
          ))}
        </div>
        <div>
          <Skeleton className="h-6 w-40 mb-2" />
          <div className="space-y-2">
            <Skeleton className="h-4 w-full" />
            <Skeleton className="h-4 w-full" />
            <Skeleton className="h-4 w-5/6" />
          </div>
        </div>
      </CardContent>
    </Card>
  );
}
